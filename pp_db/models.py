class TransferBonus(Base):
    __tablename__ = "transfer_bonuses"

    id: Mapped[int] = mapped_column(BigInteger, Identity(always=False), primary_key=True)
    bank_program_id: Mapped[int] = mapped_column(SmallInteger, nullable=False)
    airline_code: Mapped[str] = mapped_column(String(10), nullable=False)
    bonus_pct: Mapped[int] = mapped_column(Integer, nullable=False)
    starts_at: Mapped[date] = mapped_column(Date, nullable=False)
    ends_at: Mapped[date] = mapped_column(Date, nullable=False)
    notes: Mapped[str | None] = mapped_column(String)
    created_at_utc: Mapped[datetime] = mapped_column(DateTime, nullable=False, server_default=_NOW)
    updated_at_utc: Mapped[datetime] = mapped_column(DateTime, nullable=False, server_default=_NOW)

    __table_args__ = (
        CheckConstraint("bonus_pct > 0"),
        CheckConstraint("ends_at >= starts_at"),
        UniqueConstraint("bank_program_id", "airline_code", "starts_at"),
        Index("idx_transfer_bonuses_active", "airline_code", "starts_at", "ends_at"),
    )


